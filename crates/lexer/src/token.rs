#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Let,
    Fn,
    Return,
    If,
    Else,
    True,
    False,
    Null,
    Identifier(String),
    Number(f64),
    String(String),
    Plus,
    Minus,
    Asterisk,
    Slash,
    Equal,
    NotEqual,
    LessThan,
    LessThanOrEqual,
    GreaterThan,
    GreaterThanOrEqual,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Comma,
    Semicolon,
    Dot,
    EOF,
}
